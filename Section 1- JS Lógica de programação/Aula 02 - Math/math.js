console.log("Hello World!");

let num1 = 9.678465;
console.log(num1);

// arredondar para baixo 
// FLOOR
let down = Math.floor(num1);
console.log(down);

// arredondar para cima
// CEIL
let up = Math.ceil(num1);
console.log(up);

// Arredonda para o mais próximo
// ROUND
let round = Math.round(num1);
console.log(round);

// Maior número de uma sequência
// Math.max
console.log(Math.max(1, 7, 989, 10000, 2, 45));

// Menor número de uma sequência
// Math.min
console.log(Math.min(1, 7, 989, 10000, 2, 45));

// Gerar um número aleatório
// Math.random
const aleatory = Math.floor(Math.random() * (10 - 5) + 5);
console.log(aleatory)

// Valor de PI
console.log(Math.PI);

// Potênciia
console.log(Math.pow(5, 15))

// Raiz quadrada
console.log(num1 ** (1/2));